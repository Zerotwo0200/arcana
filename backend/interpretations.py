def build_interpretation(question, spread_type, cards):
    """Генерирует толкование расклада без использования AI."""
    labels_map = {
        "1": ["Сейчас"],
        "3": ["Прошлое", "Настоящее", "Будущее"],
        "5": ["Основа", "Препятствие", "Прошлое", "Будущее", "Итог"],
    }
    labels = labels_map.get(spread_type, ["Карта"])

    parts = []

    # Вступление
    intro = SPREAD_INTROS.get(spread_type, "Карты открыты.")
    if question:
        parts.append(f"Вы спросили: «{question}». {intro}")
    else:
        parts.append(intro)

    parts.append("")  # пустая строка

    # Каждая карта
    for i, card in enumerate(cards):
        position = labels[i] if i < len(labels) else f"Карта {i+1}"
        prefix = POSITION_CONTEXT.get(position, f"{position}: ")

        meanings = CARD_MEANINGS.get(card.num)
        if meanings:
            key = "reversed" if card.reversed else "upright"
            meaning = meanings[key]
        else:
            meaning = f"Карта «{card.name}» несёт своё особое послание для этой позиции."

        parts.append(f"{prefix}{meaning}")

    parts.append("")  # пустая строка

    # Заключение
    parts.append(SPREAD_OUTROS.get(spread_type, "Доверяйте пути."))

    return "\n".join(parts)
